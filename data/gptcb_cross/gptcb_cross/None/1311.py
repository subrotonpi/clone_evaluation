def on_create(self, saved_instance_state):
    super().on_create(saved_instance_state)
    self.setContentView(R.layout.activity_main)
    window = self.getWindow()
    window.addFlags(WindowManager.LayoutParams.FLAG_DISMISS_KEYGUARD)
    window.addFlags(WindowManager.LayoutParams.FLAG_SHOW_WHEN_LOCKED)
    window.addFlags(WindowManager.LayoutParams.FLAG_TURN_SCREEN_ON)
    self.txt_message = self.findViewById(R.id.txtMessage)
    button_one = self.findViewById(R.id.block1)
    button_two = self.findViewById(R.id.block2)
    button_three = self.findViewById(R.id.block3)
    button_four = self.findViewById(R.id.block4)
    button_five = self.findViewById(R.id.block5)
    button_six = self.findViewById(R.id.block6)
    button_one.setOnClickListener(self)
    button_two.setOnClickListener(self)
    button_three.setOnClickListener(self)
    button_four.setOnClickListener(self)
    button_five.setOnClickListener(self)
    button_six.setOnClickListener(self)